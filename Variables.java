package JavaPractice;

public class Variables {

	public static void main(String[] args) {
		//string
		String name="Abhinav";
		System.out.println(name);
		
		//Integer
		int a=20;
		System.out.println(a);
		
		//Float
		Float num=5.20f;
		System.out.println(num);
		
		//Char
		char myletter='A';
		System.out.println(myletter);
		
		//Boolean
		boolean myBool=true;
		System.out.println(myBool);
		
	

	}

}
