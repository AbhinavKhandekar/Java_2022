package Assignment_2;

public class Demo_1 {
	
//1.Java Program to Add, subtract, multiply and divide Two Numbers
	
	public static void main(String[] args) {
		int a= 20;
		int b=10;
		
		//Add 2 Numbers
		int add=a+b;
		System.out.println("Addition of 2 numbers : "+add);
		
		//Subtract 2 Numbers
		int sub=a-b;
		System.out.println("Subtraction of 2 numbers : "+sub);
		
		//Multiplay of 2 number
		int mul=a*b;
		System.out.println("Multiplication of 2 Numbers : "+mul);
		
		//Dividation of 2 Numbers
		int div=a/b;
		System.out.println("Dividation of 2 Numbers : "+div);

	}

}
