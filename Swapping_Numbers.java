package Assignment_2;

public class Swapping_Numbers {

	public static void main(String[] args) {
	//4.Java Program to Swap Two Numbers
		int a= 20;
		int b=10;
		
		System.out.println("Before swapping : "+a+" "+b);
		
		int t=a;
		a=b;
		b=t;
		System.out.println("After Swapping : "+a+ " "+b);

	}

}
