package JavaPractice;

public class Casting {

	public static void main(String[] args) {
		//Widening Casting
		int num=20;
		double num1=num;
		System.out.println(num);
		System.out.println(num1);
		
		//Narrowing Casting
		double mynum=25000;
		int mynum1=(int) mynum;
		System.out.println(mynum);
		System.out.println(mynum1);
		

	}

}
